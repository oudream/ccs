using System;
using System.Collections.Generic;
using System.Text;

namespace Vpa
{
    /// <summary>
    /// 分隔符单元
    /// </summary>
    public class xSeparator
    {
        /// <summary>
        /// "."点分隔符数组
        /// </summary>
        public readonly static string[] DOT = new string[] { "." };
        /// <summary>
        /// ","逗号分隔符数组
        /// </summary>
        public readonly static string[] COMMA = new string[] { "," };
        /// <summary>
        /// ";"分号分隔符数组
        /// </summary>
        public readonly static string[] SEMICOLON = new string[] { ";" };
        /// <summary>
        /// "="等号分隔符数组
        /// </summary>
        public readonly static string[] EQUAL = new string[] { "=" };
        /// <summary>
        /// ":"冒号分隔符数组
        /// </summary>
        public readonly static string[] COLON = new string[] { ":" };
        /// <summary>
        /// "*"星号分隔符数组
        /// </summary>
        public readonly static string[] ASTERISK = new string[] { "*" };
    }
}
